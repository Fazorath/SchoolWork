## 3.3 Your Own List
## Think of your favorite mode of transportation, such as a 
## motorcycle or a car, and make a list that stores several examples. Use your list 
## to print a series of statements about these items, such as “I would like to own a 
## Honda motorcycle.”

## My own list
transportation = ['Motorcycle','Airplane','Car','Submarine']

## Various print statements with different uses of lists.
print(f"If you want to drown at the bottom of the ocean.. Use a {transportation[3]}")
print(f"I can imagine riding a {transportation[0]} is great fun")
print(f"{transportation[1]}s are safe but {transportation[2]}s are much cooler")
print(f"i drive a Chevy {transportation[2]}")
